package main

import (
	"errors"
	"log"
	"strings"
)

type Char struct {
	Open     string
	Close    string
	LastSeen bool
	Count    int
}

var points = map[string]int{
	")": 3,
	"]": 57,
	"}": 1197,
	">": 25137,
}

func main() {
	lines := strings.Split(input, "\n")
	illegals := []string{}
	for _, line := range lines {
		chars := map[string]Char{
			"()": {"(", ")", false, 0},
			"[]": {"[", "]", false, 0},
			"{}": {"{", "}", false, 0},
			"<>": {"<", ">", false, 0},
		}

		illegal := illegalChar(strings.Split(line, ""), chars)
		if illegal != "" {
			log.Println("syntax error", illegal)
			illegals = append(illegals, illegal)
		}
	}
	sum := 0
	for _, ill := range illegals {
		sum += points[ill]
	}
	log.Println(sum)
}

func illegalChar(line []string, chars map[string]Char) string {
	var depth = []string{}
	for _, char := range line {
		// log.Println("depth", depth, "progress", progress)
		c, err := FindChar(char, chars)
		if err != nil {
			log.Fatal(err)
		}

		if char == c.Open {
			depth = append(depth, c.Close)
		}
		if char == c.Close {
			if len(depth) == 0 {
				return char
			}
			var x string
			x, depth = depth[len(depth)-1], depth[:len(depth)-1]
			if x != char {
				return char
			}
		}
	}
	return ""
}

func FindChar(c string, charMap map[string]Char) (Char, error) {
	for idx, val := range charMap {
		if strings.Contains(idx, c) {
			return val, nil
		}
	}
	return Char{}, errors.New("not found")
}

